// Fonction qui fait des operations mathemathiques basique.


function basicOp(operation, value1, value2)
{
  if(operation === '+'){
    return value1 + value2;
  }
  if(operation === '-'){
    return value1 - value2;
  }
  if(operation === '*'){
    return value1 * value2;
  }
    if(operation === '/'){
    return value1 / value2;
  }
}